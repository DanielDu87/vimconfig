--==============================================================================
-- 诊断设置：已移至 lua/plugins/diagnostics.lua（LazyVim 正规方式）
--==============================================================================

--==============================================================================
-- 折叠设置（修复 LSP folding 错误）
--==============================================================================

-- 使用 treesitter 折叠（而非 LSP folding）
vim.opt.foldmethod = "expr"
vim.opt.foldexpr = "v:lua.vim.treesitter.foldexpr()"
vim.opt.foldtext = ""

-- 默认展开所有折叠
vim.opt.foldenable = true
vim.opt.foldlevel = 99

--==============================================================================
-- LSP Inlay Hints 设置
--==============================================================================

-- 始终显示 inlay hints（不仅在光标附近）
vim.lsp.inlay_hint.enable(true)

--==============================================================================
-- 界面与交互设置
--==============================================================================

-- 始终开启自动换行
vim.opt.wrap = true

--==============================================================================
-- 全局自定义变量
--==============================================================================

-- 指定默认浏览器路径 (Google Chrome Beta)
vim.g.browser_path = "/Applications/Google Chrome Beta.app"
