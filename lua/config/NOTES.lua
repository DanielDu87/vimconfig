--==============================================================================
-- LazyVim 提示功能配置
--==============================================================================
-- 本文件说明 LazyVim 自带的所有提示功能都是开启状态

-- ✅ 已开启的 LazyVim 提示功能：

-- 1. 插件更新通知
--    在 lua/config/lazy.lua 中已设置：
--    checker = { enabled = true, notify = true }
--    当有插件更新时会自动通知

-- 2. 代码诊断提示
--    LSP 诊断信息会在代码中显示
--    错误、警告、提示等信息
--    快捷键：[d, ]d 跳转诊断

-- 3. 自动补全提示
--    nvim-cmp 提供智能补全
--    自动显示补全菜单

-- 4. 快捷键提示（which-key）
--    按 <leader> 会自动显示可用的快捷键
--    按空格键会看到所有 leader 键映射

-- 5. Git 状态提示
--    gitsigns 在左侧显示 Git 变更
--    + 表示添加，~ 表示修改，- 表示删除

-- 6. 光标上下文显示
--    nvim-treesitter-context 在顶部显示当前函数/类名

-- 7. 彩虹括号
--    不同层级的括号显示不同颜色

-- 8. 缩进参考线
--    indent-blankline 显示缩进线

-- 9. 颜色代码预览
--    CSS/HTML 中的颜色代码会显示实际颜色

-- 10. 代码操作提示
--     LazyVim 会自动提示可用的代码操作
--     例如：自动导入、重命名、提取函数等

-- 11. 命令行提示
--     命令行自动补全
--     : 命令会显示可用命令列表

-- 12. 文件类型提示
--     自动检测文件类型并加载相应配置

-- 13. LSP 进度提示
--     LSP 服务器加载时会显示进度

-- 14. Mason 通知
--     LSP 服务器安装/更新时显示通知

-- 15. 搜索结果预览
--     Telescope/ grep 搜索时会实时显示结果

--==============================================================================
-- 注意：本文件仅为说明文档，不包含任何配置代码
--==============================================================================
--
-- 所有 LazyVim 默认功能都保持开启状态
-- 没有禁用任何提示功能
--
-- 如需查看特定功能的配置，请参考：
-- - lua/config/lazy.lua - 插件管理器配置
-- - lua/config/options.lua - 基础选项
-- - lua/config/keymaps.lua - 键位映射
-- - lua/config/autocmds.lua - 自动命令
--
-- 查看 LazyVim 官方默认配置：
