-- 文件类型检测
-------------------------------------------------------------------------------

-- 将文件名包含 "docker" 或 "dk" 的文件识别为 dockerfile（忽略大小写）
vim.filetype.add({
	pattern = {
		-- 匹配文件名包含 docker（忽略大小写）
		[".*[Dd][Oo][Cc][Kk][Ee][Rr].*"] = "dockerfile",
		-- 匹配文件名包含 dk（忽略大小写）
		[".*[Dd][Kk].*"] = "dockerfile",
	},
})
