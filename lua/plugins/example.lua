--==============================================================================
-- LazyVim 插件配置示例
--==============================================================================
-- 本文件展示如何配置 LazyVim 插件
--
-- 重要提示：
-- 这只是一个示例文件，默认被禁用（见下方的 return {} 语句）
-- 要使用这些配置，请删除或注释掉 "return {}" 这一行
--
-- 官方文档：
-- https://lazyvim.github.io/configuration#adding-plugins

--==============================================================================
-- 示例配置开关
--==============================================================================
-- 将此行改为注释以启用示例配置
if true then
	return {}
end

--==============================================================================
-- 插件配置示例
--==============================================================================
return {

	-------------------------------------------------------------------------
	-- 示例 1: 导入 LazyVim 官方 extras（推荐方式）
	-------------------------------------------------------------------------
	-- LazyVim 官方提供了许多预配置的语言和工具支持
	-- 完整列表: https://lazyvim.github.io/extras

	-- TypeScript/JavaScript 支持
	{ import = "lazyvim.plugins.extras.lang.typescript" },

	-- Python 支持
	{ import = "lazyvim.plugins.extras.lang.python" },

	-- Go 支持
	{ import = "lazyvim.plugins.extras.lang.go" },

	-- Rust 支持
	{ import = "lazyvim.plugins.extras.lang.rust" },

	-- JSON/YAML 支持
	{ import = "lazyvim.plugins.extras.lang.json" },

	-- Tailwind CSS 支持
	{ import = "lazyvim.plugins.extras.lang.tailwind" },

	-------------------------------------------------------------------------
	-- 示例 2: 更改配色方案
	-------------------------------------------------------------------------
	{
		"LazyVim/LazyVim",
		opts = {
			colorscheme = "tokyonight", -- 默认主题
			-- 其他可选主题: "catppuccin", "gruvbox", "nord"
		},
	},

	-------------------------------------------------------------------------
	-- 示例 3: 配置 treesitter（添加语法解析器）
	-------------------------------------------------------------------------
	{
		"nvim-treesitter/nvim-treesitter",
		opts = function(_, opts)
			-- 添加需要确保安装的解析器
			vim.list_extend(opts.ensure_installed, {
				"python",
				"typescript",
				"tsx",
			})
		end,
	},

	-------------------------------------------------------------------------
	-- 示例 4: 配置 Mason（安装 LSP 和工具）
	-------------------------------------------------------------------------
	{
		"williamboman/mason.nvim",
		opts = {
			ensure_installed = {
				"stylua",      -- Lua 格式化
				"black",       -- Python 格式化
				"prettierd",   -- JS/TS 格式化
				"shellcheck",  -- Shell 检查
			},
		},
	},

	-------------------------------------------------------------------------
	-- 示例 5: 配置 LSP 服务器
	-------------------------------------------------------------------------
	{
		"neovim/nvim-lspconfig",
		opts = {
			servers = {
				-- pyright 会自动安装
				pyright = {},
			},
		},
	},

	-------------------------------------------------------------------------
	-- 示例 6: 添加新插件
	-------------------------------------------------------------------------
	-- 添加 gruvbox 配色方案
	{ "ellisonleao/gruvbox.nvim" },

	-------------------------------------------------------------------------
	-- 示例 7: 禁用 LazyVim 插件
	-------------------------------------------------------------------------
	-- { "folke/trouble.nvim", enabled = false },
}

