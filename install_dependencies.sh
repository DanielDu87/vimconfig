#!/bin/bash
#==============================================================================
# Neovim 配置依赖自动安装脚本
#==============================================================================
# 此脚本只安装基础工具（非 LSP），所有语言服务器由 Mason 管理
# 使用方法: ./install_dependencies.sh

set -e  # 遇到错误立即退出

echo "========================================"
echo "开始安装 Neovim 配置依赖..."
echo "========================================"

#------------------------------------------------------------------------------
# 检查系统
#------------------------------------------------------------------------------
if [[ "$OSTYPE" == "darwin"* ]]; then
    echo "检测到 macOS 系统"
    PKG_MANAGER="brew"
elif command -v apt-get &> /dev/null; then
    echo "检测到 Debian/Ubuntu 系统"
    PKG_MANAGER="apt"
else
    echo "不支持的系统，请手动安装依赖"
    exit 1
fi

#------------------------------------------------------------------------------
# 检查 Homebrew (macOS)
#------------------------------------------------------------------------------
if [[ "$PKG_MANAGER" == "brew" ]] && ! command -v brew &> /dev/null; then
    echo "错误: 未找到 Homebrew，请先安装:"
    echo "/bin/bash -c \"\$(curl -fsSL https://raw.githubusercontent.com/Homebrew/install/HEAD/install.sh)\""
    exit 1
fi

#------------------------------------------------------------------------------
# 安装基础工具（非 LSP）
#------------------------------------------------------------------------------
echo ""
echo "----------------------------------------"
echo "安装基础工具..."
echo "----------------------------------------"

if [[ "$PKG_MANAGER" == "brew" ]]; then
    # macOS
    brew install \
        fd \
        ripgrep \
        fzf \
        lazygit \
        tree-sitter || true
else
    # Linux
    sudo apt-get update
    sudo apt-get install -y \
        fd-find \
        ripgrep \
        fzf \
        lazygit \
        tree-sitter || true
fi

echo "✓ 基础工具安装完成"

#------------------------------------------------------------------------------
# 安装 Neovim（如未安装）
#------------------------------------------------------------------------------
echo ""
echo "----------------------------------------"
echo "检查 Neovim..."
echo "----------------------------------------"

if ! command -v nvim &> /dev/null; then
    echo "安装 Neovim..."
    if [[ "$PKG_MANAGER" == "brew" ]]; then
        brew install neovim
    else
        sudo apt-get install -y neovim
    fi
else
    echo "Neovim 已安装: $(nvim --version | head -1)"
fi

#------------------------------------------------------------------------------
# 安装 Neovim 插件和 Mason 工具
#------------------------------------------------------------------------------
echo ""
echo "----------------------------------------"
echo "安装 Neovim 插件..."
echo "----------------------------------------"

echo "同步 Lazy.nvim 插件..."
nvim --headless "+Lazy! sync" +qa

echo "✓ Neovim 插件安装完成"

#------------------------------------------------------------------------------
# 说明
#------------------------------------------------------------------------------
echo ""
echo "========================================"
echo "基础依赖安装完成！"
echo "========================================"
echo ""
echo "语言服务器由 Mason 管理，会自动安装"
echo ""
echo "已安装的基础工具:"
echo "  - fd: 快速文件查找"
echo "  - ripgrep: 内容搜索"
echo "  - fzf: 模糊查找"
echo "  - lazygit: Git UI"
echo "  - tree-sitter: 语法解析"
echo ""
echo "接下来:"
echo "1. 启动 Neovim: nvim"
echo "2. Mason 会自动安装所有语言服务器"
echo "3. 打开任意文件即可开始使用"
echo "4. 查看已安装工具: :Mason"
echo ""
